/* MQ-1 Sensör Direnci Hesaplama */
float MQ1_GetResistance(uint32_t raw_adc) {
    if (raw_adc == 0) {
        return -1.0; // ADC sıfırsa hata döndür
    }

    float voltage = (float)raw_adc / 4096.0 * 5.0; // ADC'yi voltaja çevir (Vcc = 5V)
    if (voltage <= 0 || voltage >= 5.0) {
        return -1.0; // Geçersiz voltaj durumunda hata döndür
    }

    float resistance = RL_VALUE * (5.0 - voltage) / voltage; // Vcc = 5.0
    return resistance > 0 ? resistance : -1.0; // Negatifse hata döndür
}


/* PPM Hesaplama */
float MQ1_CalculatePPM(float *curve, float rs_ro_ratio) {
    if (rs_ro_ratio <= 0) {
        return -1.0; // Hata durumunda NaN döndür
    }

    return pow(10.0, ((log10(rs_ro_ratio) - curve[0]) / curve[1]));
}

/* Ro Kalibrasyonu */
float MQ1_CalibrateRo(uint32_t *adc_samples, size_t sample_count) {
    float avg_resistance = 0.0;

    for (size_t i = 0; i < sample_count; i++) {
        float resistance = MQ1_GetResistance(adc_samples[i]);
        if (resistance > 0) {
            avg_resistance += resistance;
        }
    }

    avg_resistance /= sample_count;

    if (avg_resistance > 0) {
        return avg_resistance / RO_CLEAN_AIR_FACTOR;
    } else {
        return -1.0; // Kalibrasyon başarısız
    }
}
